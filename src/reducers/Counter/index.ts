import { Counter, Action } from "./actions";

const CountReducer = (state: Counter, action: Action) => {
  switch (action.type) {
    case "increment":
      return { count: state.count + 1 };
    case "decrement":
      return { count: state.count - 1 };
    default:
      return state;
  }
};

export default CountReducer;
